		function filteredEntries (scope) 
		{
			var filtered = [];
			
			var startDate = new Date(scope.start_yr.date, scope.start_month.order, 1);
			var endDate = new Date(scope.end_yr.date, scope.end_month.order, 31);

			for (var i = 0; i < scope.entries.length; i++) {
				var entryDate = new Date(scope.entries[i].date);

				if (entryDate.getTime() >= startDate.getTime() && entryDate.getTime() <= endDate.getTime()) {
					filtered.push(scope.entries[i]);
				}
			}
	
			return filtered;
		}

		function sortEntries (a, b) 
